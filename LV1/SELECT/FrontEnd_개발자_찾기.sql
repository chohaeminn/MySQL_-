-- 문제: FrontEnd 개발자 찾기 (Front End 카테고리의 스킬을 하나라도 보유한 개발자 조회)
--
-- 사용 문법
-- SUM(CODE) : 특정 카테고리(Front End)에 속하는 모든 스킬의 비트 값을 하나로 합산
-- 비트 연산자 (&) : 개발자의 SKILL_CODE와 프론트엔드 스킬 전체 합산 값을 비교하여 하나라도 겹치는지 체크
-- 서브쿼리 (Subquery) : 조건에 맞는 스킬들의 코드 합을 동적으로 구하여 메인 쿼리에 전달
-- ORDER BY 컬럼 ASC : ID 기준 오름차순 정렬
--
-- 배운 점
-- 1. 단일 스킬명(Python, C#)이 아니라 특정 '카테고리(그룹)' 전체를 검사해야 할 때는 서브쿼리 내에서 SUM() 함수를 사용해 비트 플래그를 하나로 결합할 수 있다.
-- 2. 각 스킬 코드가 2의 제곱수(2진수 비트 자리)로 구성되어 있기 때문에, SUM()으로 더하더라도 비트들이 서로 간섭하거나 오염되지 않고 독립된 스위치 역할을 유지한다.
-- 3. 개발자의 SKILL_CODE와 프론트엔드 전체 합산 값(SUM)을 앤(&) 연산했을 때 결과가 0이 아니면(참이면), 해당 카테고리 스킬을 최소 하나 이상 가지고 있다는 뜻이 된다.
--
-- 느낀 점
-- 이전 문제에서 특정 스킬 하나를 찾던 방식을 확장하여, 카테고리 전체의 비트를 SUM으로 묶어 처리하는 응용 방식을 배우며 비트 연산의 무궁무진한 활용성을 깨달았다. 복잡해 보이는 조건도 비트마스크 개념을 한 번 제대로 정립해 두니 쿼리가 군더더기 없이 깔끔하게 짜여서 신기했다.

SELECT ID, EMAIL, FIRST_NAME, LAST_NAME
FROM DEVELOPERS
WHERE 
    SKILL_CODE & (SELECT SUM(CODE) FROM SKILLCODES WHERE CATEGORY = 'Front End')
ORDER BY ID ASC;
