-- 문제: 잡은 물고기 중 길이가 10cm 이하인 물고기의 수 출력하기
--
-- 사용 문법
-- COUNT(*) : 테이블 내의 전체 행(Row) 개수를 집계하는 내장 함수
-- WHERE 컬럼 IS NULL : 데이터가 비어 있는(NULL) 행만 필터링 (문제 조건에서 10cm 이하는 NULL로 저장됨)
-- AS 별칭 : 결과 컬럼의 이름을 지정된 명칭(FISH_COUNT)으로 변경
--
-- 배운 점
-- 1. COUNT와 같은 내장 집계 함수를 사용할 때는 함수명(COUNT)과 괄호'(*)' 사이에 공백(띄어쓰기)이 포함되면 문법 에러(Syntax Error)가 발생하므로 반드시 붙여서 작성해야 한다.
-- 2. 문제 설명에서 '10cm 이하일 경우 LENGTH가 NULL'이라는 규칙을 지정해 주었으므로, 숫자 비교(<= 10)가 아닌 'IS NULL' 조건을 사용하는 것이 정답의 핵심이다.
--
-- 느낀 점
-- 완벽한 필터링 조건을 세웠더라도 함수 괄호 앞의 사소한 공백 하나 때문에 전체 쿼리가 실패할 수 있다는 점에서 SQL 문법의 엄격함을 배웠다. 에러 메시지를 눈여겨보며 오타를 잡아내는 디테일한 역량을 기를 수 있었던 좋은 경험이었다.

SELECT COUNT(*) AS FISH_COUNT
FROM FISH_INFO
WHERE LENGTH IS NULL;
