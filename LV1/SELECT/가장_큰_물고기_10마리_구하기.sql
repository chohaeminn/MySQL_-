-- 문제: 가장 큰 물고기 10마리의 ID와 길이 출력하기
--
-- 사용 문법
-- WHERE 컬럼 IS NOT NULL : 데이터가 비어 있지 않은(값이 존재하는) 행만 필터링
-- ORDER BY 컬럼1 DESC, 컬럼2 ASC : 1차 기준으로 내림차순 정렬 후, 값이 같으면 2차 기준으로 오름차순 정렬
-- LIMIT 숫자 : 출력할 결과의 행(Row) 개수를 제한하여 상위 N개만 조회
--
-- 배운 점
-- 1. 대량의 데이터 중 '가장 큰 데이터 N개' 혹은 '상위 N개'를 추출하고 싶을 때는 ORDER BY로 정렬한 뒤 맨 마지막에 LIMIT를 조합하여 해결할 수 있다.
-- 2. ORDER BY 절에 쉼표(,)를 기준으로 여러 컬럼을 나열하면, 첫 번째 정렬 기준의 값이 동일할 때 적용할 두 번째, 세 번째 정렬 기준을 차례대로 지정할 수 있다.
--
-- 느낀 점
-- 정렬 조건이 여러 개(길이 내림차순, ID 오름차순)라 처음엔 복잡해 보일 수 있었지만, 배운 정렬 문법을 차근차근 적용하니 생각보다 쉽게 풀렸다. 에러 없이 한 번에 '성공' 메시지를 확인했을 때 짜릿한 성취감을 느꼈고, SQL 작성에 점점 자신감이 생기는 것 같다.

SELECT ID, LENGTH
FROM FISH_INFO
WHERE LENGTH IS NOT NULL
ORDER BY LENGTH DESC, ID ASC
LIMIT 10;
